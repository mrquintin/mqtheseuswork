"""
Meta-Analytical Layer for Noosphere.

This module performs real-time methodological analysis of substantive discourse.
The firm's brain stores only METHODOLOGICAL knowledge, but most podcast discussion
is SUBSTANTIVE (about what's true in the world). The Meta-Analytical Layer sits on
top of the classifier and transforms substantive discourse into methodological
insights — asking "what method is being used here? Is it reliable? What would make
it more rigorous?"

This module is the bridge between substance and method. When founders discuss a
substantive topic, the meta-analytical layer generates the methodological
observations that the brain actually stores.
"""

from __future__ import annotations

import json
