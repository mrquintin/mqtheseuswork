# Five-criterion meta-method gates (The Meta Method) — firm conclusions
# ═══════════════════════════════════════════════════════════════════════════


from dataclasses import dataclass as _meta_dataclass
from typing import Literal as _MetaLiteral

import numpy as _meta_np

MetaDomain = _MetaLiteral[
    "empirical_science",
    "historical_claim",
    "normative_philosophy",
    "predictive_forecast",
    "methodological_rule",
]

DOMAIN_CONFIDENCE_CEILING: dict[str, float] = {
    "empirical_science": 0.85,
    "historical_claim": 0.75,
    "normative_philosophy": 0.70,
    "predictive_forecast": 0.65,
    "methodological_rule": 0.80,
}


@_meta_dataclass
class ClaimClusterMeta:
    claim_ids: list[str]
    texts: list[str]
    centroid_embedding: Optional[list[float]] = None
    claimed_confidence: float = 0.5
    domain: MetaDomain = "normative_philosophy"
    adversarial_fingerprint: str = ""
    adversarial_store: Any | None = None


@_meta_dataclass
class CriterionResult:
    score: float
    reasoning: str


def criterion_progressivity(cluster: ClaimClusterMeta) -> CriterionResult:
    if len(cluster.texts) < 2:
        return CriterionResult(0.4, "Too few claims to assess progressivity.")
    lens = [len(t) for t in cluster.texts]
    var = float(_meta_np.var(_meta_np.asarray(lens, dtype=float)))
    score = float(_meta_np.clip(0.35 + min(1.0, var / 500.0), 0.0, 1.0))
    return CriterionResult(score, "Lexical variance proxy for progressivity.")


def criterion_severity(cluster: ClaimClusterMeta) -> CriterionResult:
    from noosphere.config import get_settings

    s = get_settings()
    if (
        s.adversarial_enforce
        and cluster.adversarial_fingerprint
        and cluster.adversarial_store is not None
    ):
        from noosphere.adversarial import adversarial_severity_criterion

        score, reason = adversarial_severity_criterion(
            cluster.adversarial_store,
            cluster.adversarial_fingerprint,
            int(s.adversarial_k),
        )
        return CriterionResult(score, reason)
    joined = " ".join(cluster.texts).lower()
    hedge = sum(1 for w in ("maybe", "perhaps", "sort of", "kind of") if w in joined)
    score = float(_meta_np.clip(0.75 - 0.08 * hedge, 0.0, 1.0))
    return CriterionResult(score, f"Hedge-density indicator (count={hedge}).")


def criterion_coherence(cluster: ClaimClusterMeta) -> CriterionResult:
    if not cluster.centroid_embedding or len(cluster.texts) < 2:
        return CriterionResult(0.55, "No centroid; neutral coherence.")
    return CriterionResult(0.72, "Centroid present; placeholder cohesion score.")


def criterion_compressibility(cluster: ClaimClusterMeta) -> CriterionResult:
    u = len(set(" ".join(cluster.texts).lower().split()))
    tot = max(1, sum(len(t.split()) for t in cluster.texts))
    ratio = u / tot
    score = float(_meta_np.clip(ratio * 1.4, 0.0, 1.0))
    return CriterionResult(score, f"Unique-token ratio={ratio:.3f}.")


def criterion_domain_sensitivity(cluster: ClaimClusterMeta) -> CriterionResult:
    cap = DOMAIN_CONFIDENCE_CEILING.get(cluster.domain, 0.7)
    if cluster.claimed_confidence > cap + 1e-6:
        return CriterionResult(
            0.0,
            f"Claimed confidence {cluster.claimed_confidence:.2f} exceeds ceiling {cap:.2f} for {cluster.domain}.",
        )
    headroom = max(0.0, cap - cluster.claimed_confidence)
    score = float(_meta_np.clip(0.5 + headroom, 0.0, 1.0))
    return CriterionResult(score, f"Within domain ceiling {cap:.2f}.")


META_THRESHOLDS: dict[str, float] = {
    "progressivity": 0.45,
    "severity": 0.55,
    "coherence": 0.50,
    "compressibility": 0.40,
    "domain_sensitivity": 0.50,
}


@_meta_dataclass
class MetaGateOutcome:
    passed_all: bool
    scores: dict[str, float]
    reasons: dict[str, str]
    route_open_question: bool
    failed_criteria: list[str]


def evaluate_five_meta_criteria(cluster: ClaimClusterMeta) -> MetaGateOutcome:
    checks = {
        "progressivity": criterion_progressivity,
        "severity": criterion_severity,
        "coherence": criterion_coherence,
        "compressibility": criterion_compressibility,
        "domain_sensitivity": criterion_domain_sensitivity,
    }
    scores: dict[str, float] = {}
    reasons: dict[str, str] = {}
    failed: list[str] = []
    for name, fn in checks.items():
        r = fn(cluster)
        scores[name] = r.score
        reasons[name] = r.reasoning
        if r.score < META_THRESHOLDS[name]:
            failed.append(name)
    pr = scores["progressivity"] < META_THRESHOLDS["progressivity"]
    sev = scores["severity"] < META_THRESHOLDS["severity"]
    route_oq = pr or sev or bool(failed)
    passed_all = not failed
    return MetaGateOutcome(
        passed_all=passed_all,
        scores=scores,
        reasons=reasons,
        route_open_question=route_oq,
        failed_criteria=failed,
    )
