# ── MetaAnalyzer ────────────────────────────────────────────────────────────

class MetaAnalyzer:
    """
    Performs real-time methodological analysis of substantive discourse.

    This is the bridge between the classifier (which identifies WHAT KIND of
    discourse is happening) and the brain (which stores only METHODOLOGICAL
    knowledge). The MetaAnalyzer transforms substantive claims into methodological
    observations that can be stored and learned from.
    """

    def __init__(
        self,
        client: Optional[Anthropic] = None,
        conclusions_registry: Optional[dict] = None
    ):
        """
        Initialize the meta-analyzer.

        Args:
            client: Optional Anthropic client. If None, creates a new one.
            conclusions_registry: Optional dict mapping method names to accuracy data.
        """
        self.client = client or Anthropic()
        self.conclusions_registry = conclusions_registry or {}
        logger.info("Initialized MetaAnalyzer")

    def analyze_discourse_segment(
        self,
        claims: list[ClassifiedClaim],
        context: str = ""
    ) -> list[MethodologicalObservation]:
        """
        Generate methodological observations from a batch of classified claims.

        Takes a segment of substantive discourse and generates methodological
        observations about the reasoning methods used, their quality, and gaps.

        Args:
            claims: List of ClassifiedClaim objects from a discourse segment
            context: Optional context about the topic being discussed

        Returns:
            List of MethodologicalObservation objects
        """
        # Filter to only substantive and mixed claims
        substantive_claims = [
            c for c in claims
            if c.discourse_type in (DiscourseType.SUBSTANTIVE, DiscourseType.MIXED)
        ]

        if not substantive_claims:
            logger.info("No substantive claims to analyze in segment")
            return []

        # Build prompt with claims
        claims_text = "\n".join(
            f"{i+1}. {c.text}"
            for i, c in enumerate(substantive_claims)
        )

        prompt = f"""{DISCOURSE_ANALYSIS_PROMPT}

Topic context: {context or "(no context)"}

Substantive claims:
{claims_text}

Generate methodological observations about these claims."""

        try:
            response = self.client.messages.create(
                model="claude-3-5-sonnet-20241022",
                max_tokens=1500,
                messages=[{"role": "user", "content": prompt}]
            )

            result_text = response.content[0].text.strip()

            # Extract JSON array from response
            observations = self._parse_observations_response(
                result_text,
                [c.claim_id for c in substantive_claims],
                claims[0].claim_id.split("_")[0]  # Use first claim's episode_id
                if substantive_claims else ""
            )

            logger.info(
                f"Generated {len(observations)} methodological observations "
                f"from {len(substantive_claims)} claims"
            )
            return observations

        except APIError as e:
            logger.error(f"Error analyzing discourse segment: {e}")
            return []

    def analyze_reasoning_chain(
        self,
        substantive_claims: list[ClassifiedClaim],
        context: str = ""
    ) -> list[MethodologicalObservation]:
        """
        Analyze the logic of a sequence of substantive claims.

        Takes a chain of claims that form an argument and evaluates the logical
        structure: is it deductive? inductive? Where are the gaps? What are the
        implicit premises?

        Args:
            substantive_claims: Ordered list of claims forming a reasoning chain
            context: Optional context about the domain

        Returns:
            List of MethodologicalObservation objects analyzing the chain logic
        """
        if not substantive_claims:
            return []

        # Build prompt with ordered claims
        claims_text = "\n".join(
            f"{i+1}. {c.text}"
            for i, c in enumerate(substantive_claims)
        )

        prompt = f"""{REASONING_CHAIN_PROMPT}

Domain context: {context or "(no context)"}

Reasoning chain (in order):
{claims_text}

Analyze the logical structure of this reasoning chain."""

        try:
            response = self.client.messages.create(
                model="claude-3-5-sonnet-20241022",
                max_tokens=1500,
                messages=[{"role": "user", "content": prompt}]
            )

            result_text = response.content[0].text.strip()

            observations = self._parse_observations_response(
                result_text,
                [c.claim_id for c in substantive_claims],
                substantive_claims[0].claim_id.split("_")[0]
                if substantive_claims else ""
            )

            logger.info(
                f"Generated {len(observations)} observations from reasoning chain analysis"
            )
            return observations

        except APIError as e:
            logger.error(f"Error analyzing reasoning chain: {e}")
            return []

    def detect_method_patterns(
        self,
        all_observations: list[MethodologicalObservation],
        window_episodes: int = 5
    ) -> list[MethodologicalObservation]:
        """
        Analyze accumulated observations to detect recurring methodological patterns.

        Looks across multiple episodes to identify:
        - Which reasoning methods dominate in which domains
        - What methods are rarely used (blind spots)
        - Patterns of change over time
        - Systematic biases in methodology

        Args:
            all_observations: All MethodologicalObservation objects accumulated
            window_episodes: How many recent episodes to analyze

        Returns:
            List of MethodologicalObservation objects describing patterns
        """
        if not all_observations:
            logger.info("No observations to analyze for patterns")
            return []

        # Count observation types and methods mentioned
        observation_counts: Counter = Counter()
        method_mentions: Counter = Counter()

        for obs in all_observations:
            observation_counts[obs.observation_type.value] += 1

            # Extract method names from observation text
            text_lower = obs.observation.lower()
            for method_name in [
                "analogical reasoning",
                "inductive reasoning",
                "deductive reasoning",
                "empirical analysis",
                "first-principles",
                "base rate",
                "authority",
                "pattern matching",
                "thought experiment",
            ]:
                if method_name in text_lower:
                    method_mentions[method_name] += 1

        if not method_mentions:
            logger.info("No specific method mentions found in observations")
            return []

        # Build pattern analysis prompt
        pattern_summary = "\n".join(
            f"- {method}: {count} mentions"
            for method, count in method_mentions.most_common()
        )

        observation_summary = "\n".join(
            f"- {obs_type}: {count} observations"
            for obs_type, count in observation_counts.most_common()
        )

        prompt = f"""{METHOD_PATTERN_PROMPT}

OBSERVATION SUMMARY (across {len(all_observations)} observations):
{observation_summary}

METHOD MENTIONS:
{pattern_summary}

Generate pattern observations about recurring methodological habits."""

        try:
            response = self.client.messages.create(
                model="claude-3-5-sonnet-20241022",
                max_tokens=1000,
                messages=[{"role": "user", "content": prompt}]
            )

            result_text = response.content[0].text.strip()

            # Parse pattern observations
            try:
                observations_data = json.loads(result_text)
                if not isinstance(observations_data, list):
                    observations_data = [observations_data]
            except json.JSONDecodeError:
                logger.warning("Could not parse pattern observations as JSON")
                return []

            pattern_observations = []
            for obs_data in observations_data:
                try:
                    pattern_observations.append(
                        MethodologicalObservation(
                            id=str(uuid.uuid4()),
                            observation=obs_data.get("observation", ""),
                            observation_type=MethodologicalObservationType.METHOD_PATTERN,
                            source_type=SourceType.PATTERN_DETECTED,
                            source_claims=[],
                            episode_id="multi_episode_pattern",
                            confidence=obs_data.get("confidence", 0.7),
                            disciplines=[],
                        )
                    )
                except Exception as e:
                    logger.warning(f"Error creating pattern observation: {e}")
                    continue

            logger.info(f"Generated {len(pattern_observations)} pattern observations")
            return pattern_observations

        except APIError as e:
            logger.error(f"Error detecting method patterns: {e}")
            return []

    def generate_methodological_feedback(
        self,
        substantive_claim_text: str,
        method_used: MethodAttributionType,
        historical_accuracy: Optional[float] = None
    ) -> str:
        """
        Generate methodological feedback on a specific substantive claim.

        Given a claim and the method used to produce it, generates feedback
        about the methodology: its reliability, implicit assumptions, and
        suggested improvements.

        Args:
            substantive_claim_text: The substantive claim
            method_used: The MethodAttributionType that produced it
            historical_accuracy: Optional 0-1 value for this method's accuracy

        Returns:
            String containing methodological feedback
        """
        accuracy_context = ""
        if historical_accuracy is not None:
            accuracy_pct = int(historical_accuracy * 100)
            accuracy_context = f"\nHistorical accuracy of {method_used.value} " \
                             f"in this domain: {accuracy_pct}%"

        prompt = f"""{FEEDBACK_PROMPT}

Claim: {substantive_claim_text}
Method used: {method_used.value}
{accuracy_context}

Provide methodological feedback."""

        try:
            response = self.client.messages.create(
                model="claude-3-5-sonnet-20241022",
                max_tokens=500,
                messages=[{"role": "user", "content": prompt}]
            )

            feedback = response.content[0].text.strip()
            logger.info("Generated methodological feedback")
            return feedback

        except APIError as e:
            logger.error(f"Error generating feedback: {e}")
            return f"Unable to generate feedback: {e}"

    def bridge_to_brain(
        self,
        observations: list[MethodologicalObservation]
    ) -> list[dict]:
        """
        Convert MethodologicalObservations into a format suitable for the brain.

        Transforms observations into structured data that can be promoted to
        full Principles in the ontology graph. The output includes fields for
        disciplines, conviction level, and evidence trail.

        Args:
            observations: List of MethodologicalObservation objects

        Returns:
            List of dicts with keys: text, disciplines, conviction_level, evidence
        """
        brain_ready = []

        for obs in observations:
            principle_dict = {
                "text": obs.observation,
                "disciplines": obs.disciplines or [Discipline.EPISTEMOLOGY],
                "conviction_level": self._confidence_to_conviction(obs.confidence),
                "conviction_score": obs.confidence,
                "evidence": {
                    "source_type": obs.source_type.value,
                    "source_claims": obs.source_claims,
                    "episode_id": obs.episode_id,
                    "created_at": obs.created_at.isoformat(),
                },
                "tags": [obs.observation_type.value],
            }
            brain_ready.append(principle_dict)

        logger.info(f"Prepared {len(brain_ready)} observations for brain ingestion")
        return brain_ready

    # ── Helpers ──────────────────────────────────────────────────────────────

    def _parse_observations_response(
        self,
        response_text: str,
        source_claim_ids: list[str],
        episode_id: str
    ) -> list[MethodologicalObservation]:
        """
        Parse JSON response into MethodologicalObservation objects.

        Args:
            response_text: Raw response from Claude
            source_claim_ids: List of claim IDs that prompted these observations
            episode_id: Which episode the observations come from

        Returns:
            List of MethodologicalObservation objects
        """
        try:
            observations_data = json.loads(response_text)
            if not isinstance(observations_data, list):
                observations_data = [observations_data]
        except json.JSONDecodeError:
            logger.warning("Could not parse observations as JSON")
            return []

        observations = []
        for obs_data in observations_data:
            try:
                observation_type_str = obs_data.get(
                    "observation_type",
                    "method_usage"
                )
                observation_type = MethodologicalObservationType(observation_type_str)

                obs = MethodologicalObservation(
                    id=str(uuid.uuid4()),
                    observation=obs_data.get("observation", ""),
                    observation_type=observation_type,
                    source_type=SourceType.INFERRED_FROM_DISCOURSE,
                    source_claims=source_claim_ids,
                    episode_id=episode_id,
                    confidence=float(obs_data.get("confidence", 0.7)),
                    disciplines=[Discipline.EPISTEMOLOGY],
                )
                observations.append(obs)
            except (KeyError, ValueError) as e:
                logger.warning(f"Error parsing observation: {e}")
                continue

        return observations

    @staticmethod
    def _confidence_to_conviction(confidence: float) -> ConvictionLevel:
        """
        Convert a confidence score (0-1) to a ConvictionLevel.

        Args:
            confidence: 0-1 confidence value

        Returns:
            ConvictionLevel enum value
        """
        if confidence >= 0.9:
            return ConvictionLevel.AXIOM
        elif confidence >= 0.75:
            return ConvictionLevel.STRONG
        elif confidence >= 0.6:
            return ConvictionLevel.MODERATE
        elif confidence >= 0.4:
            return ConvictionLevel.EXPLORATORY
        else:
            return ConvictionLevel.CONTESTED


# ── DiscourseMonitor ────────────────────────────────────────────────────────

class DiscourseMonitor:
    """
    Main integration point for processing entire episodes.

    Orchestrates the full pipeline:
    1. Classifies all claims in an episode
    2. Routes claims to appropriate destinations (brain, conclusions registry)
    3. Decomposes mixed claims
    4. Performs meta-analysis on substantive discourse
    5. Promotes strongest observations to principles
    6. Generates episode methodology report
    """

    def __init__(
        self,
        classifier,
        meta_analyzer: MetaAnalyzer,
        conclusions_registry: dict,
        ontology_graph
    ):
        """
        Initialize the discourse monitor.

        Args:
            classifier: DiscourseClassifier instance
            meta_analyzer: MetaAnalyzer instance
            conclusions_registry: Dict for storing substantive conclusions
            ontology_graph: OntologyGraph instance (the brain)
        """
        self.classifier = classifier
        self.meta_analyzer = meta_analyzer
        self.conclusions_registry = conclusions_registry
        self.ontology_graph = ontology_graph
        logger.info("Initialized DiscourseMonitor")

    def process_episode_claims(
        self,
        claims: list[Claim],
        episode_id: str,
        episode_date: date
    ) -> dict:
        """
        Process all claims from an episode: classify, route, analyze, store.

        This is the main integration point. For each episode, it:
        1. Classifies each claim
        2. Routes METHODOLOGICAL and META_METHODOLOGICAL to the brain
        3. Routes SUBSTANTIVE to conclusions registry (with method attribution)
        4. Decomposes MIXED claims
        5. Runs meta-analysis to extract methodological observations
        6. Promotes strongest observations to principles
        7. Returns a summary report

        Args:
            claims: List of Claim objects from the episode
            episode_id: Episode identifier
            episode_date: Episode date

        Returns:
            Dict with summary: claim_counts_by_type, new_principles_found,
            new_conclusions_registered, methodological_observations_generated
        """
        logger.info(f"Processing {len(claims)} claims from episode {episode_id}")

        # Phase 1: Classify all claims
        classified_claims = []
        for claim in claims:
            classified = self.classifier.classify(
                claim.text,
                context=claim.segment_context,
                claim_id=claim.id
            )
            classified_claims.append(classified)

        # Phase 2: Route and decompose
        methodological_claims = []
        substantive_claims = []
        mixed_claims = []
        conclusions_to_register = []

        for classified in classified_claims:
            if classified.discourse_type == DiscourseType.METHODOLOGICAL:
                methodological_claims.append(classified)
                # Route to brain
                self._route_methodological_to_brain(classified, episode_id)

            elif classified.discourse_type == DiscourseType.META_METHODOLOGICAL:
                # Route to brain as well
                self._route_methodological_to_brain(classified, episode_id)

            elif classified.discourse_type == DiscourseType.SUBSTANTIVE:
                substantive_claims.append(classified)
                # Register as conclusion
                conclusions_to_register.append(classified)

            elif classified.discourse_type == DiscourseType.MIXED:
                mixed_claims.append(classified)
                # Decompose and route components
                self._decompose_and_route_mixed(classified, episode_id)
                # Also analyze substantive component
                if classified.substantive_content:
                    substantive_claims.append(classified)

        # Phase 3: Meta-analysis on substantive discourse
        all_substantive = substantive_claims + mixed_claims
        methodological_observations = (
            self.meta_analyzer.analyze_discourse_segment(
                all_substantive,
                context=f"Episode {episode_id}"
            )
        )

        # Phase 4: Route strong observations to brain as principles
        promoted_count = 0
        for obs in methodological_observations:
            if obs.confidence >= 0.75:  # Only promote strong observations
                self._promote_observation_to_principle(obs)
                promoted_count += 1

        # Build summary report
        summary = {
            "episode_id": episode_id,
            "episode_date": episode_date.isoformat(),
            "claims_processed": len(claims),
            "claims_by_type": {
                "methodological": len(methodological_claims),
                "substantive": len(substantive_claims),
                "mixed": len(mixed_claims),
                "meta_methodological": sum(
                    1 for c in classified_claims
                    if c.discourse_type == DiscourseType.META_METHODOLOGICAL
                ),
                "non_propositional": sum(
                    1 for c in classified_claims
                    if c.discourse_type == DiscourseType.NON_PROPOSITIONAL
                ),
            },
            "conclusions_registered": len(conclusions_to_register),
            "methodological_observations_generated": len(methodological_observations),
            "observations_promoted_to_principles": promoted_count,
        }

        logger.info(
            f"Episode {episode_id} processing complete: "
            f"{summary['claims_by_type']['methodological']} methodological, "
            f"{summary['claims_by_type']['substantive']} substantive, "
            f"{len(methodological_observations)} observations generated"
        )

        return summary

    def episode_methodology_report(self, episode_id: str) -> dict:
        """
        Generate a report on an episode's methodological character.

        After processing an episode, produces a detailed report on:
        - Ratio of methodological to substantive discourse
        - Methods used in substantive reasoning
        - Methodological gaps identified
        - New methodological observations
        - Recommendations for future discussions

        Args:
            episode_id: Episode identifier

        Returns:
            Dict with methodology report
        """
        # This would require storing observations per episode
        # For now, return a template structure
        report = {
            "episode_id": episode_id,
            "methodology_character": {
                "substantive_ratio": 0.0,
                "methodological_ratio": 0.0,
            },
            "methods_used": {},
            "methodological_gaps": [],
            "new_observations": [],
            "recommendations": [],
        }

        logger.info(f"Generated methodology report for episode {episode_id}")
        return report

    # ── Routing Helpers ──────────────────────────────────────────────────────

    def _route_methodological_to_brain(
        self,
        claim: ClassifiedClaim,
        episode_id: str
    ) -> None:
        """Route a methodological claim to the ontology graph."""
        try:
            principle = Principle(
                text=claim.text,
                description=claim.decomposition_notes,
                disciplines=[Discipline.EPISTEMOLOGY],
                conviction=ConvictionLevel.EXPLORATORY,
                conviction_score=claim.confidence,
            )
            self.ontology_graph.add_principle(principle)
            logger.debug(f"Routed methodological claim to brain: {claim.text[:50]}")
        except Exception as e:
            logger.warning(f"Error routing methodological claim: {e}")

    def _decompose_and_route_mixed(
        self,
        claim: ClassifiedClaim,
        episode_id: str
    ) -> None:
        """Decompose a mixed claim and route its components."""
        # Methodological component goes to brain
        if claim.methodological_content:
            try:
                principle = Principle(
                    text=f"Method: {claim.methodological_content}",
                    disciplines=[Discipline.EPISTEMOLOGY],
                    conviction=ConvictionLevel.EXPLORATORY,
                    conviction_score=claim.confidence * 0.9,  # Slightly lower
                )
                self.ontology_graph.add_principle(principle)
            except Exception as e:
                logger.warning(f"Error routing methodological component: {e}")

    def _promote_observation_to_principle(
        self,
        observation: MethodologicalObservation
    ) -> None:
        """Promote a high-confidence observation to a principle."""
        try:
            principle = Principle(
                text=observation.observation,
                disciplines=observation.disciplines or [Discipline.EPISTEMOLOGY],
                conviction=DiscourseMonitor._confidence_to_conviction(
                    observation.confidence
                ),
                conviction_score=observation.confidence,
                supporting_claims=observation.source_claims,
                tags=[observation.observation_type.value],
                first_appeared=date.today(),
            )
            self.ontology_graph.add_principle(principle)
            logger.info(
                f"Promoted observation to principle: {observation.observation[:50]}"
            )
        except Exception as e:
            logger.warning(f"Error promoting observation to principle: {e}")

    @staticmethod
    def _confidence_to_conviction(confidence: float) -> ConvictionLevel:
        """
        Convert a confidence score (0-1) to a ConvictionLevel.

        Args:
            confidence: 0-1 confidence value

        Returns:
            ConvictionLevel enum value
        """
        if confidence >= 0.9:
            return ConvictionLevel.AXIOM
        elif confidence >= 0.75:
            return ConvictionLevel.STRONG
        elif confidence >= 0.6:
            return ConvictionLevel.MODERATE
        elif confidence >= 0.4:
            return ConvictionLevel.EXPLORATORY
        else:
            return ConvictionLevel.CONTESTED


